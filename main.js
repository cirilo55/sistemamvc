//sistema de Pop-up ;
function openPopup() {
    const popupContainer = document.getElementById('popupContainer');
    popupContainer.style.display = 'block';
}

// Função para fechar o popup
function closePopup() {
    const popupContainer = document.getElementById('popupContainer');
    popupContainer.style.display = 'none';
}
